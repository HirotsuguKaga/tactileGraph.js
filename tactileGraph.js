    function splitByLength(str, length) {
      var resultArr = [];
      if (!str || !length || length < 1) {
        return resultArr;
      }
      var index = 0;
      var start = index;
      var end = start + length;
      while (start < str.length) {
        resultArr[index] = str.substring(start, end);
        index++;
        start = end;
        end = start + length;
      }
      return resultArr;
    }
